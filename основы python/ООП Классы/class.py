class Student:
    def __init__(self, name):
        self.name = name
        self.grades = []  # Список для хранения оценок

    def add_grade(self, grade):
        self.grades.append(grade)  # Добавляем новую оценку

    def average_grade(self):
        if self.grades:
            return sum(self.grades) / len(self.grades)  # Вычисляем среднюю оценку
        return 0.0  # Если оценок нет, возвращаем 0


