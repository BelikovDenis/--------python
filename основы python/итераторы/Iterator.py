"""
Типичные ошибки


Тип ошибки 1: Повторное использование истощённого итератора

Ошибка возникает после того как итератор полностью перебран,
его нельзя использовать повторно — он становится «истощённым».

Пример ошибки:

numbers = [1, 2, 3]
iterator = iter(numbers)

# Перебираем элементы один раз
for num in iterator:
    print(num)  # Вывод: 1, 2, 3

# Пытаемся перебрать элементы второй раз
for num in iterator:
    print(num)  # Ничего не выведет, итератор пуст



Решение:

Если нужно перебрать данные повторно, создайте новый итератор.

numbers = [1, 2, 3]

# Создаём новый итератор при каждом переборе
for num in iter(numbers):
    print(num)

for num in iter(numbers):  # Новый итератор
    print(num)













Тип ошибки 2: Пропущенное исключение StopIteration

Ошибка возникает,
если вручную вызывать next() и элементы итератора заканчиваются,
Python выбросит исключение StopIteration.
Это может завершить программу с ошибкой, если исключение не обработано.

Пример ошибки:

numbers = [1, 2, 3]
iterator = iter(numbers)

print(next(iterator))  # Вывод: 1
print(next(iterator))  # Вывод: 2
print(next(iterator))  # Вывод: 3
print(next(iterator))  # Ошибка: StopIteration




Решение:

Используйте блок try-except, чтобы обработать исключение StopIteration.

numbers = [1, 2, 3]
iterator = iter(numbers)

while True:
    try:
        print(next(iterator))
    except StopIteration:
        break  # Выходим из цикла, когда элементы заканчиваются








Тип ошибки 3: Использование итератора в другой части программы

Итератор «запоминает», где мы остановились, поэтому,
если мы передадим итератор в другую часть программы,
элементы могут быть пропущены или повторно недоступны.


Пример ошибки:

numbers = [1, 2, 3]
iterator = iter(numbers)

# Используем итератор в одном цикле
for num in iterator:
    print(num)  # Вывод: 1, 2, 3

# Передаём тот же итератор в другой цикл
for num in iterator:
    print(num)  # Ничего не выведет, итератор пуст


Решение:

Создавайте новый итератор, если он нужен в другой части программы.

numbers = [1, 2, 3]

# Используем новый итератор для каждой задачи
iterator1 = iter(numbers)
iterator2 = iter(numbers)

for num in iterator1:
    print(num)

for num in iterator2:
    print(num)













Тип ошибки 4: Неверный объект передан в iter() — TypeError

Функция iter() принимает только итерируемые объекты.
Если передать объект, который не поддерживает итерацию,
Python выбросит TypeError.



Пример ошибки:

number = 123  # Число, а не коллекция
iterator = iter(number)  # Ошибка: TypeError


Решение:

Убедитесь, что мы используем итерируемый объект,
такой как список, строка, множество или кортеж.

numbers = [1, 2, 3]  # Список — итерируемый объект
iterator = iter(numbers)  # Работает



Эти примеры помогут нам
избежать распространённых ошибок при работе с итераторами.
Итераторы полезны,
но их нужно использовать правильно, чтобы избежать неожиданных ситуаций!
"""



















