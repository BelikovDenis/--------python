import sqlite3
from datetime import datetime, timedelta


def create_database():
    """Создание базы данных и таблицы"""
    conn = sqlite3.connect('customers.db')
    cursor = conn.cursor()
    
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS Customers (
            CustomerID INTEGER PRIMARY KEY,
            FirstName TEXT,
            LastName TEXT,
            Email TEXT,
            Phone TEXT,
            RegistrationDate TEXT,
            IsActive INTEGER
        )
    ''')
    
    initial_customers = [
        (1, 'John', 'Doe', 'johndoe@example.com', 
         '+1234567890', '2025-01-10', 1),
        (2, 'Alice', 'Smith', 'alicesmith@example.com', 
         '+1234567891', '2024-12-15', 1),
        (3, 'Bob', 'Johnson', 'bobjohnson@example.com', 
         '+1234567892', '2024-11-20', 0),
        (4, 'Eve', 'Davis', 'evedavis@example.com', 
         '+1234567893', '2025-01-05', 1),
        (5, 'Charlie', 'Brown', 'charliebrown@example.com', 
         '+1234567894', '2024-10-25', 0)
    ]
    
    cursor.executemany('''
        INSERT OR REPLACE INTO Customers 
        (CustomerID, FirstName, LastName, Email, Phone, RegistrationDate, 
        IsActive) VALUES (?, ?, ?, ?, ?, ?, ?)
    ''', initial_customers)
    
    conn.commit()
    return conn


def add_new_customers(conn):
    """Добавление новых клиентов"""
    cursor = conn.cursor()
    
    new_customers = [
        (6, 'David', 'Miller', 'davidmiller@example.com', 
         '+1234567895', '2024-11-01', 1),
        (7, 'Grace', 'Wilson', 'gracewilson@example.com', 
         '+1234567896', '2024-12-01', 0),
        (8, 'Helen', 'Moore', 'helenmoore@example.com', 
         '+1234567897', '2024-09-15', 0)
    ]
    
    cursor.executemany('''
        INSERT INTO Customers 
        (CustomerID, FirstName, LastName, Email, Phone, RegistrationDate, 
        IsActive) VALUES (?, ?, ?, ?, ?, ?, ?)
    ''', new_customers)
    
    print("Добавлены новые клиенты:")
    for customer in new_customers:
        print(f"  - {customer[1]} {customer[2]}")
    
    conn.commit()


def update_customer_status(conn):
    """Обновление статуса активности клиентов"""
    cursor = conn.cursor()
    
    cursor.execute('''
        UPDATE Customers 
        SET IsActive = 1 
        WHERE IsActive = 0 AND RegistrationDate < '2024-12-05'
    ''')
    
    updated_count = cursor.rowcount
    print(f"Обновлено статусов активности: {updated_count}")
    
    cursor.execute('''
        SELECT CustomerID, FirstName, LastName, RegistrationDate 
        FROM Customers 
        WHERE IsActive = 1 AND RegistrationDate < '2024-12-05'
    ''')
    
    updated_customers = cursor.fetchall()
    print("Клиенты, у которых обновлен статус на активный:")
    for customer in updated_customers:
        reg_date = customer[3]
        print(f"  - {customer[1]} {customer[2]} (зарегистрирован {reg_date})")
    
    conn.commit()


def delete_inactive_customers(conn):
    """Удаление неактивных клиентов старше 60 дней"""
    cursor = conn.cursor()
    
    cursor.execute('''
        SELECT CustomerID, FirstName, LastName, RegistrationDate 
        FROM Customers 
        WHERE IsActive = 0 AND RegistrationDate < '2024-11-10'
    ''')
    
    customers_to_delete = cursor.fetchall()
    
    cursor.execute('''
        DELETE FROM Customers 
        WHERE IsActive = 0 AND RegistrationDate < '2024-11-10'
    ''')
    
    deleted_count = cursor.rowcount
    print(f"Удалено неактивных клиентов: {deleted_count}")
    
    print("Удаленные клиенты:")
    for customer in customers_to_delete:
        reg_date = customer[3]
        print(f"  - {customer[1]} {customer[2]} (зарегистрирован {reg_date})")
    
    conn.commit()


def display_all_customers(conn):
    """Отображение всех текущих клиентов"""
    cursor = conn.cursor()
    cursor.execute('SELECT * FROM Customers')
