class User:
    def __init__(self, id, name, email):
        self._id = id       # Приватный атрибут
        self._name = name   # Приватный атрибут (будет установлен через сеттер для валидации)
        self._email = email # Приватный атрибут (будет установлен через сеттер для валидации)
        
        # Используем сеттеры для инициализации с валидацией
        self.name = name
        self.email = email
    
    # Геттер для id
    @property
    def id(self):
        return self._id
    
    # Геттер для name
    @property
    def name(self):
        return self._name
    
    # Сеттер для name
    @name.setter
    def name(self, value):
        if not value or value.strip() == "":
            raise ValueError("Имя не может быть пустым")
        self._name = value
    
    # Геттер для email
    @property
    def email(self):
        return self._email
    
    # Сеттер для email
    @email.setter
    def email(self, value):
        if "@" not in value:
            raise ValueError("Некорректный email")
        self._email = value
    
    def save(self):
        # Логика сохранения в базу данных
        print(f"Сохранение пользователя: {self.name}, email: {self.email}")

