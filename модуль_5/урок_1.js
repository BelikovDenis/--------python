let input = prompt("Введите дробное число:");

let floatNumber = parseFloat(input);
let roundedDown = parseInt(floatNumber);

if (isNaN(floatNumber)) {
    console.log("Это не число");
} else {
    console.log(`${floatNumber} — это примерно ${roundedDown}`);
}










