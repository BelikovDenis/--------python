def count_tasks_from_list(subtasks_list,
                          parent_name="главной задачи", depth=0):
    indent = "  " * depth
    print(f"{indent}→ Запускаем count_tasks_from_list для подзадач "
          f"'{parent_name}' (уровень {depth})")
    
    total = 0
    for i, task in enumerate(subtasks_list, 1):
        task_name = task.get("name", f"задача {i}")
        print(f"{indent}  Обрабатываем: '{task_name}'")
        
        subtasks_count = count_tasks_from_list(
            task.get("subtasks", []), task_name, depth + 1
        )
        
        task_total = 1 + subtasks_count
        print(f"{indent}  Результат для '{task_name}': 1 + {subtasks_count} = "
              f"{task_total}")
        total += task_total
    
    print(f"{indent}← Возвращаем общее количество для подзадач "
          f"'{parent_name}': {total}")
    return total


