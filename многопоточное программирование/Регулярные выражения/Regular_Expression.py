# Импорт модуля для работы с регулярными выражениями
import re

# Определение текста для анализа
text = "Серверы доступны по адресам: 192.168.1.1, 256.256.256.256, 127.0.0.1, 0.0.0.0, 300.300.300.300."

# Создание регулярного выражения для поиска IP-адресов
pattern = r'\b(?:\d{1,3}\.){3}\d{1,3}\b'

# Поиск всех IP-адресов в тексте
ip_addresses = re.findall(pattern, text)

# Фильтрация корректных IP-адресов
valid_ips = []
for ip in ip_addresses:
    parts = ip.split('.')
    if all(0 <= int(part) <= 255 for part in parts):
        valid_ips.append(ip)

# Вывод найденных корректных IP-адресов
print("Корректные IP-адреса:", valid_ips)





